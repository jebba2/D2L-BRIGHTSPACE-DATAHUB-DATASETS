CREATE TABLE IF NOT EXISTS DiscussionPosts (
    PostId BIGINT NOT NULL,
    TopicId BIGINT NOT NULL,
    UserId INT,
    OrgUnitId INT,
    ThreadId BIGINT,
    Thread VARCHAR(400),
    IsReply TINYINT(1),
    ParentPostId BIGINT,
    NumReplies INT,
    DatePosted DATETIME NOT NULL,
    IsDeleted TINYINT(1),
    RatingSum BIGINT,
    NumRatings BIGINT,
    Score DECIMAL(19, 9),
    LastEditDate DATETIME,
    SortOrder INT,
    Depth INT,
    WordCount INT,
    AttachmentCount INT,
    Version BIGINT,
    PRIMARY KEY (PostId)
) CHARACTER SET utf8mb4;